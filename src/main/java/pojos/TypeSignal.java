package pojos;

/**
 * Enumeration of supported signal types in the system.
 * Used to classify different types of biomedical signals that can be processed.
 */
public enum TypeSignal {
    /**
     * Electrocardiogram - measures electrical activity of the heart
     */
    ECG,
    /**
     * Electromyogram - measures electrical activity produced by skeletal muscles
     */
    EMG
}
