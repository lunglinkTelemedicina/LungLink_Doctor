package pojos;

/**
 * Represents the different medical specialties that doctors can have in the LungLink system.
 * Used to categorize doctors based on their field of expertise.
 */
public enum DoctorSpecialty {
    /**
     * Specialist in heart and cardiovascular system disorders
     */
    CARDIOLOGIST,
    /**
     * Specialist in nervous system function and disorders
     */
    NEUROPHYSIOLOGIST,
    /**
     * General practitioner providing primary healthcare
     */
    GENERAL_MEDICINE
}
